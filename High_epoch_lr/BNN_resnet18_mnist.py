class Binarize(tf.keras.constraints.Constraint):
    def __init__(self, ref_value = 1):
            self.ref_value = ref_value

    def __call__(self, w):
            return tf.math.sign(w)*self.ref_value

    def get_config(self):
            return {'ref_value': self.ref_value}
    

